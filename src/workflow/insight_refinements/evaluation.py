"""
Evaluation Logic for QAI Optimization Framework.

This module provides classes and functions for comparing predicted analysis
results against golden references and calculating performance metrics.
"""

import pandas as pd
import logging
from typing import Any, Dict, List
from dataclasses import dataclass
from sklearn.metrics import (
    precision_recall_fscore_support,
    cohen_kappa_score,
    confusion_matrix,
)
from tabulate import tabulate
from collections import defaultdict


# Configure logging
logger = logging.getLogger(__name__)


@dataclass
class QuestionEvaluation:
    """
    Data container for a single question evaluation.

    Attributes:
        question_id (str): Unique identifier for the question.
        question_text (str): The body of the question.
        golden_answer (str): The ground truth answer.
        predicted_answer (str): The answer generated by the model.
    """

    question_id: str
    question_text: str
    golden_answer: str
    predicted_answer: str

    @property
    def is_correct(self) -> bool:
        """Checks if the predicted answer matches the golden answer (case-insensitive)."""
        return (
            str(self.golden_answer).strip().lower()
            == str(self.predicted_answer).strip().lower()
        )


class ConversationEvaluator:
    """
    Evaluates a set of question results for a single resource/conversation.
    """

    def __init__(
        self,
        resource_id: str,
        evaluations: List[QuestionEvaluation],
        valid_labels: List[str] = None,
    ):
        """
        Initializes the ConversationEvaluator.

        Args:
            resource_id (str): Identifier for the resource being evaluated.
            evaluations (List[QuestionEvaluation]): List of individual question evaluations.
            valid_labels (List[str]): List of valid labels for metric calculation (e.g., ['yes', 'no', 'n/a']).
        """
        self.resource_id = resource_id
        self.evaluations = evaluations
        self.valid_labels = [
            label.lower()
            for label in (
                valid_labels
                or ["meets", "partial meets", "does not meet", "n/a", "yes", "no"]
            )
        ]

    def compute_metrics(self) -> Dict[str, Any]:
        """
        Computes overall metrics for the conversation.

        Returns:
            Dict[str, Any]: Dictionary containing accuracy, precision, recall, f1 score, and cohen's kappa.
        """
        y_true = [str(e.golden_answer).lower() for e in self.evaluations]
        y_pred = [str(e.predicted_answer).lower() for e in self.evaluations]

        # Filter for labels present in valid_labels to avoid metric noise
        present_labels = sorted(list(set(y_true + y_pred)))
        filtered_labels = [
            label for label in present_labels if label in self.valid_labels
        ]

        precision, recall, f1, _ = precision_recall_fscore_support(
            y_true, y_pred, labels=filtered_labels, average="weighted", zero_division=0
        )

        # Cohen's Kappa
        kappa = cohen_kappa_score(y_true, y_pred, labels=filtered_labels)
        if pd.isna(kappa):
            kappa = 0.0  # Handle case where kappa is undefined (e.g., less than 2 classes present)

        accuracy = (
            sum(1 for e in self.evaluations if e.is_correct) / len(self.evaluations)
            if self.evaluations
            else 0
        )

        return {
            "resource_id": self.resource_id,
            "accuracy": accuracy,
            "precision": precision,
            "recall": recall,
            "f1_score": f1,
            "cohens_kappa": kappa,
            "total_questions": len(self.evaluations),
        }


class EvaluationReporter:
    """
    Aggregates results across multiple conversations and generates reports.
    """

    def __init__(self, all_evaluations: Dict[str, List[QuestionEvaluation]]):
        """
        Initializes the EvaluationReporter.

        Args:
            all_evaluations (Dict[str, List[QuestionEvaluation]]): Map of resource ID to its evaluations.
        """
        self.all_evaluations = all_evaluations

    def get_summary_dataframe(self) -> pd.DataFrame:
        """
        Generates a summary DataFrame with metrics per resource.

        Returns:
            pd.DataFrame: Summary metrics.
        """
        rows = []
        for rid, evals in self.all_evaluations.items():
            evaluator = ConversationEvaluator(rid, evals)
            metrics = evaluator.compute_metrics()
            rows.append(metrics)

        return pd.DataFrame(rows)

    def get_question_level_metrics(self) -> pd.DataFrame:
        """
        Generates metrics aggregated by question ID.

        Returns:
            pd.DataFrame: Performance metrics per question.
        """
        question_map = defaultdict(list)
        for evals in self.all_evaluations.values():
            for e in evals:
                question_map[e.question_id].append(e)

        rows = []
        for qid, evals in question_map.items():
            # Simple accuracy for question level
            correct = sum(1 for e in evals if e.is_correct)
            total = len(evals)

            rows.append(
                {
                    "question_id": qid,
                    "question_text": evals[0].question_text,
                    "total_samples": total,
                    "correct": correct,
                    "accuracy": correct / total if total > 0 else 0,
                }
            )

        return pd.DataFrame(rows)

    def get_confusion_matrix(self, valid_labels: List[str] = None) -> pd.DataFrame:
        """
        Generates a confusion matrix for all evaluations.

        Returns:
            pd.DataFrame: Confusion matrix with labels as indices/columns.
        """
        y_true = []
        y_pred = []
        for evals in self.all_evaluations.values():
            for e in evals:
                y_true.append(str(e.golden_answer).lower())
                y_pred.append(str(e.predicted_answer).lower())

        labels = sorted(list(set(y_true + y_pred)))
        if valid_labels:
            labels = [label for label in labels if label in valid_labels]

        cm = confusion_matrix(y_true, y_pred, labels=labels)
        return pd.DataFrame(cm, index=labels, columns=labels)

    def print_report(self):
        """Prints a formatted report to the console."""
        summary_df = self.get_summary_dataframe()
        question_df = self.get_question_level_metrics()

        print("\n=== EVALUATION REPORT SUMMARY ===")
        print(tabulate(summary_df, headers="keys", tablefmt="pretty", showindex=False))

        print("\n=== PERFORMANCE BY QUESTION ===")
        print(tabulate(question_df, headers="keys", tablefmt="pretty", showindex=False))

        print("\n=== CONFUSION MATRIX ===")
        cm_df = self.get_confusion_matrix()
        print(tabulate(cm_df, headers="keys", tablefmt="pretty"))

        overall_accuracy = summary_df["accuracy"].mean()
        overall_kappa = summary_df["cohens_kappa"].mean()
        print(f"\nOverall Mean Accuracy: {overall_accuracy:.2%}")
        print(f"Overall Mean Cohen's Kappa: {overall_kappa:.2f}")


def run_automated_evaluation(
    results_df: pd.DataFrame, golden_df: pd.DataFrame, join_key: str = "resource_id"
) -> EvaluationReporter:
    """
    Standardizes the process of joining results and running evaluation.

    Args:
        results_df (pd.DataFrame): DataFrame containing predicted answers.
        golden_df (pd.DataFrame): DataFrame containing golden reference answers.
        join_key (str): Column name to join on (e.g., conversation_id).

    Returns:
        EvaluationReporter: Initialized reporter with joined data.
    """
    # Merge dataframes
    merged = pd.merge(
        results_df,
        golden_df,
        on=[join_key, "question_id"],
        suffixes=("_pred", "_golden"),
    )

    eval_map = defaultdict(list)
    for _, row in merged.iterrows():
        eval_obj = QuestionEvaluation(
            question_id=str(row["question_id"]),
            question_text=str(
                row.get("question_text", row.get("question_text_golden", ""))
            ),
            golden_answer=str(row["answer_golden"]),
            predicted_answer=str(row["answer_pred"]),
        )
        eval_map[str(row[join_key])].append(eval_obj)

    return EvaluationReporter(eval_map)
